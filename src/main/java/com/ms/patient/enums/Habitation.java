package com.ms.patient.enums;

/**
 * Representa os tipos de habitação (residência) de uma Pessoa ou Paciente.
 * <p>Usado para categorizar a natureza da moradia do indivíduo.</p>
 */
public enum Habitation {
    /** Unidade residencial em um prédio com múltiplas unidades. */
    APARTMENT,
    /** Residência unifamiliar ou casa independente. */
    HOUSE,
    /** Unidade residencial pequena e compacta, geralmente composta por um único cômodo integrado. */
    KITNET
}
